package Operators;

public class ArithmeticOperator {

	public static void main(String[] args) {
		// Addition, Subtraction, Multiplication, Division & modulus is arithmetic Operators
		int a=10, b=20;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);

	}

}
